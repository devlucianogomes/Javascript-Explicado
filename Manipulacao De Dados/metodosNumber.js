// Manipulação de números no Javascript

let numero = 10.12341;
let string = "25.6";

// Transformar number em STRING
console.log(numero.toString());

// Alterar o numero casas decimais
console.log(numero.toFixed(2)); // retornará 10.12

// Transformar string EM NUMBER
console.log(parseInt(string));

// Transformar string com decimal em NUMBER
console.log(parseFloat(string));
