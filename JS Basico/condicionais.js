/**
 * Condicionais
 * É executar algo, dependendo de alguma ação, ou seja
 * se acontecer isso, faça isso, caso não aconteça,
 * faça isso
 */

let hora = 21;

if (hora < 12) {
  console.log("Bom dia");
} else if (hora > 12 && hora < 18) {
  console.log("Boa tarde");
} else {
  console.log("Boa noite");
}
