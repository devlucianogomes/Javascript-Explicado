/**
 * Variáveis
 * Imagina um guarda roupas com vários compartimentos, supondo que no primeiro
 * compartimento eu queira guardar camisas, noutro camisetas, noutro gravatas.
 * Assim são as variaveis, são espalos alocados em memória para armazenaromos
 * determinado tipo de dado.
 *
 * No javascrip declaramos variaveis com var, let e const ( constantes ) ,
 * por boa prática de programação, é recomendado o uso de let como inicializador.
 *
 * Exemplo de criação de variável :
 */

// Declaração de variáveis
var idade = 24;
let ano = 2022;
let nome = "Dev's Academy";

// Após a criação da variável podemos utilizada em outro lugar do nosso código
// pois os dados ja estarão armazenados na mesma.
console.log(nome);
