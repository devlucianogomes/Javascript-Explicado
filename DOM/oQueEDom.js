/**
 * O que é ?
 * DOM = Document Object Model
 
    É a nossa árvore de elementos, ou seja, tudo que temos em tela,
    seja um botão, um texto, um elemento html, head, div, etc.
    Tudo isso pertence ao DOM, ou seja, o que é renderizado e mostrado
    em tela. Ou seja, é a referencia de cada elemento que aparece na página, é normal ouvirmos a expressão '' Vamos manipular o DOM ''.
    Que significa, que vamos alterar os elementos em tela através do JS

    Temos também o VIRTUAL DOM, que é como se fosse uma cópia 
    armazenada na memória, que não é aplicada diretamente ao DOM

 */
